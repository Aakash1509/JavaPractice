import java.util.Hashtable;

public class HashTable {
    public static void main(String[] args) {
        Hashtable h = new Hashtable();
        h.put(106,"deepak");
        h.put(102,"kamal");
        h.put(108,"ravi");
        h.put(117,"rahul");
        System.out.println(h); //Will be printed from top to bottom and left to right
    }
}
